    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txtid.getText()!=null && txtname.getText()!=null && txtexperince.getText()!=null){
            try {
                Connection conn=MySqlConnection();
                PreparedStatement psc = conn.prepareStatement("insert into dctadd"+"(did,dname,dexp,djoindate) values(?,?,?,?) ");
                psc.setInt(1,Integer.parseInt(txtid.getText()));
                psc.setString(2,txtname.getText());
                psc.setString(3, txtexperince.getText());
                
                SimpleDateFormat sd = new SimpleDateFormat("dd-MM-yyyy");
                String jnDate = sd.format(jDateChooser1.getDate());
                psc.setString(4, jnDate);
                
                int res=psc.executeUpdate();
                if(res>=1){
                    JOptionPane.showMessageDialog(null,"Add Sucessfully");
                }else{
                    JOptionPane.showMessageDialog(null, "Add failed");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }
            txtid.setText("");
            txtname.setText("");
            txtexperince.setText("");
            jDateChooser1.setDate(null);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

